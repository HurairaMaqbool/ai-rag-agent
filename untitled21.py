# Cell 13 — Full Working Agent

def agent(query):
    try:
        intent = detect_intent(query)
        print(f"🔍 Intent detected: {intent}")

        # ---------------- Calculator ----------------
        if intent == "calculator":
            return calculator_tool(query)

        # ---------------- Web Search ----------------
        elif intent == "web_search":
            web_results = web_search_tool(query)

            if isinstance(web_results, list):
                web_results = "\n".join([str(r) for r in web_results])

            return ask_llm(query, context=web_results)

        # ---------------- PDF RAG ----------------
        else:
            chunks_found = hybrid_search(query)

            if not chunks_found:
                return "❌ No relevant documents found."

            context = "\n".join([c.get("text", "") for c in chunks_found])

            sources = []
            for c in chunks_found:
                page = c.get("page", None)
                if page:
                    sources.append(f"Page {page}")
                else:
                    sources.append("Unknown Source")

            answer = ask_llm(query, context=context)

            return f"{answer}\n\n📄 Sources: {', '.join(sources)}"

    except Exception as e:
        return f"❌ Error: {str(e)}"


