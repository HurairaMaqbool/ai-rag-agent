# Cell 9 — Agent Router logic
import re

def detect_intent(query):
    query_lower = query.lower()

    # Math detection
    if re.search(r'[\d]+[\s]*[\+\-\*\/\^%][\s]*[\d]+', query):
        return "calculator"
    if any(w in query_lower for w in ["calculate", "compute", "math", "how much is", "what is", "equals"]):
        if re.search(r'\d', query):
            return "calculator"

    # Web search detection
    web_keywords = ["latest", "news", "today", "current", "2024", "2025", "recent", "who won", "price of"]
    if any(w in query_lower for w in web_keywords):
        return "web_search"

    # PDF detection
    return "pdf_rag"  # default

# Test
