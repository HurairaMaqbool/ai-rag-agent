# Cell 10 — Calculator Tool
def calculator_tool(query):
    try:
        # Extract math expression safely
        expr = re.findall(r'[\d\s\+\-\*\/\.\(\)\^%]+', query)
        if expr:
            result = eval(expr[0].strip())
            return f"Result: {result}"
        return "Could not find math expression."
    except:
        return "Math error."

# Cell 11 — Web Search Tool (FREE, no API key)
from duckduckgo_search import DDGS

def web_search_tool(query, max_results=3):
    with DDGS() as ddgs:
        results = list(ddgs.text(query, max_results=max_results))
    if not results:
        return "No results found."
    output = ""
    for r in results:
        output += f"Title: {r['title']}\nSummary: {r['body']}\n\n"
    return output

