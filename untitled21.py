# Cell — Smart Chunking (Optimized for RAG)

def chunk_text(data_items, chunk_size=700, overlap=120):
    chunks = []

    for item in data_items:
        text = item["text"]

        # source info (page or paragraph)
        identifier = item.get("page", item.get("paragraph", None))
        file_name = item["file"]

        start = 0

        while start < len(text):
            end = start + chunk_size
            chunk = text[start:end]

            # store chunk with metadata
            chunks.append({
                "text": chunk,
                "source_id": identifier,
                "source_file": file_name
            })

            start += chunk_size - overlap

    return chunks


# Run chunking
chunks = chunk_text(text_data)

print(f"✅ Created {len(chunks)} smart chunks")

