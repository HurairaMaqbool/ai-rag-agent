# Cell 8 — Hybrid Search function
def hybrid_search(query, top_k=5):
    # Semantic search (FAISS)
    q_embed = embed_model.encode([query])
    D, I = index.search(np.array(q_embed, dtype="float32"), top_k)
    semantic_results = [chunks[i] for i in I[0]]

    # Keyword search (BM25)
    scores = bm25.get_scores(query.lower().split())
    bm25_top = np.argsort(scores)[::-1][:top_k]
    keyword_results = [chunks[i] for i in bm25_top]

    # Merge (deduplicate)
    seen, merged = set(), []
    for c in semantic_results + keyword_results:
        key = c["text"][:50]
        if key not in seen:
            seen.add(key)
            merged.append(c)
    return merged[:top_k]

